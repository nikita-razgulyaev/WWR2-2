function adaptPage() {
  // Получаем ширину окна
  const windowWidth = window.innerWidth;

  // Вычисляем коэффициент масштабирования
  const zoomLevel = windowWidth / 14.4;

  // Находим элемент main
  const mainElement = document.querySelector('main');

  // Применяем свойство zoom
  if (mainElement) {
      mainElement.style.zoom = zoomLevel + '%';
  }
}

// Вызываем функцию при загрузке страницы
window.addEventListener('load', adaptPage);

// Вызываем функцию при изменении размера окна
window.addEventListener('resize', adaptPage);


document.addEventListener('DOMContentLoaded', function() {
  const tooltip = document.querySelector('.tooltip.--1');
  const tooltipText = document.querySelector('.tooltiptext.--1');

  tooltip.addEventListener('mouseenter', function() {
    tooltipText.style.visibility = 'visible';
    tooltipText.style.opacity = '1';
  });

  tooltip.addEventListener('mouseleave', function() {
    tooltipText.style.visibility = 'hidden';
    tooltipText.style.opacity = '0';
  });
});

